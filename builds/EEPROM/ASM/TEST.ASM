	LJMP START

START:
	MOV SP,#1FH
	MOV R7,#0FFH
	MOV R6,#0FFH
	MOV R0,#1
	MOV R1,#0FFH
	LCALL DELAY
	CLR P3.2
LEFT:
	JB P3.3,NOKEY
	LCALL DELAY
	CPL P3.0
	MOV R0,P3
	SETB P3.2
	LCALL DELAY
	CLR P3.2
	CPL P1.0
NOKEY:
	LCALL SHOW
;	LCALL DELAY
	SJMP LEFT


SHOW:
	MOV A,R0
	CPL A
	MOV P1,A
	RET

DELAY:
	NOP
	DJNZ R7,DELAY
	MOV R7,#0FFH
	DJNZ R6,DELAY
	MOV R6,#080H
	RET
