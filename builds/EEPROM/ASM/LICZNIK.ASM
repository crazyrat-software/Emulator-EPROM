	LJMP START

START:
	MOV SP,#1FH
	MOV R7,#0FFH
	MOV R6,#0B0H
	MOV R0,#1
	MOV R1,#0FFH

LEFT:
	LCALL SHOW
	LCALL DELAY
	MOV A,R0
	INC A
	MOV R0,A
	DJNZ R1,LEFT
	MOV R1,#0FFH
RIGHT:
	LCALL SHOW
	LCALL DELAY
	MOV A,R0
	DEC A
	MOV R0,A
	DJNZ R1,RIGHT
	MOV R1,#0FFH
	SJMP LEFT


SHOW:
	MOV A,R0
	CPL A
	MOV P1,A
	RET

DELAY:
	NOP
	DJNZ R7,DELAY
	MOV R7,#0FFH
	DJNZ R6,DELAY
	MOV R6,#0B0H
	RET
