	LJMP START

START:
	MOV SP,#1FH
	MOV R7,#0FFH
	MOV R6,#090H
	MOV R0,#1
	MOV R1,#7

LEFT:
	LCALL SHOW
	LCALL DELAY
	MOV A,R0
	RL A
	MOV R0,A
	DJNZ R1,LEFT
	MOV R1,#7
RIGHT:
	LCALL SHOW
	LCALL DELAY
	MOV A,R0
	RR A
	MOV R0,A
	DJNZ R1,RIGHT
	MOV R1,#7

	SJMP LEFT


SHOW:
	MOV A,R0
	CPL A
	MOV P1,A
	RET

DELAY:
	NOP
	DJNZ R7,DELAY
	MOV R7,#0FFH
	DJNZ R6,DELAY
	MOV R6,#090H
	RET
